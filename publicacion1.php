        <h1>El habito del ahorro en casa</h1>

        <img src="img/publicacion1.jpg" class="publicacion-img" alt="">  


        <p class="font-italic">Jessica Leal</p>

        <p class="font-weight-light">09 de septiembre de 2019</p>

        <p class="font-weight-light">El hogar es el lugar en donde adquirimos nuestros primeros hábitos, y aquellos que aprendemos para el manejo de nuestras finanzas no son la excepción. Un niño que es animado a ahorrar para cumplir un objetivo, como comprarse un juguete o aquellos dulces que tanto le gustan, será más creativo y responsable en el manejo de los recursos con que cuenta, que un niño que aprende a gastarse su domingo inmediatamente después de recibirlo.</p>

        <p class="font-weight-light">El manejo de las finanzas en el hogar, es responsabilidad de todos los que integran la familia; así es, de todos los que forman parte de ella, desde el más pequeño que pide dinero para comprarse un dulce, hasta el padre o madre de familia que sale a trabajar para aportar económicamente. Es por ello que este espacio quiero aprovecharlo para compartirte algunos tips que he aprendido y puesto en práctica para contribuir a las finanzas del hogar:</p>

        <ul>
          <li>
            <p class="font-weight-light"><span class="font-weight-bold">Reduce el monto de los recibos. </span>Ahorrar no es tan complicado cuando nos damos cuenta que existen muchas formas de hacerlo. Hace algunas semanas me visitó mi sobrino de 4 años, quien me seguía a todos lados a donde yo iba, saliendo tras de mí y apagando cada luz que yo dejaba encendida, así es, tuvo que recordarme en todo momento que yo iba saliendo de la cocina o de mi habitación sin apagar la luz, y aunque es complicado reconocerlo, debo aceptar que el más pequeño de la familia me recordó algo que parece obvio, pero que en ocasiones olvidamos: las facturas que se pagan mes con mes por los diferentes servicios del hogar (luz, agua, gas, teléfono), pueden disminuir si nuestro consumo se vuelve más responsable.</p>
            <p class="font-weight-light">Cuida el agua cerrando bien todas las llaves, utiliza un recipiente donde hay una fuga o mientras se calienta el agua de la regadera para que puedas hacer uso de esa agua más adelante, si te es posible, utiliza calentadores solares, no contrates o elimina aquelllos servicios que no utilices, ¡ah! y muy importante, apaga las luces cada vez que una habitación se encuentre vacía, créeme, tus recibos, el medio ambiente y mi sobrino, te lo agradecerán.</p>
          </li>

          <li>
            <p class="font-weight-light"><span class="font-weight-bold">Repara a tiempo.  </span>Seguramente alguna vez dentro de tu casa se ha averiado algo: la cafetera, la licuadora, el microondas, etcétera, pues bien, aunque hay ocasiones en que las cosas que sufren desperfectos nos facilitan grandemente la vida diaria, no siempre las reparamos de inmediato, sino que creemos que pueden esperar, lo cual ocasiona que el desperfecto se haga aún mayor o que incluso no tenga solución. Por ejemplo, ¿Qué pasaría si la tubería de tu casa sufre un daño y no la arreglas de inmediato? Lo más probable es que el daño que ha sufrido sea más grave unas semanas después, lo que generará un mayor costo a la hora de arreglarlo, y permíteme decirte que si lo habías dejado pasar porque no había recursos para la reparación, ¡oh sorpresa! ahora se requiere un recurso mayor y con más urgencia. Así que cuando se trate del mantenimiento de los utensilios, aparatos o instalaciones de tu hogar, recuerda no dejar para mañana lo que puedas hacer hoy.</p>
          </li>
          
          <li> 
            <p class="font-weight-light"><span class="font-weight-bold">No hagas el super sin una lista. </span>¿Te ha pasado que recorres los pasillos del supermercado y no estás seguo si te hace falta algo, lo llevas a casa y resulta que no lo necesitabas? Acudir al super para cubrir ciertas necesidades es fundamental, sin embargo, esto puede volverse un problema para nuestros bolsillos si terminamos adquiriendo algo que no necesitamos. Revisar los artículos que tienes en casa y llevar una lista contigo de los productos que vas a comprar, antes de llegar al super, puede ayudar a evitar las compras compulsivas.</p>
            <p class="font-weight-light">Los supermercados son lugares que cuentan con diversidad de marcas y productos que lucen agradables a nuestra vista, pero que no son indispensables para la mayoría de los casos, así que llevar una lista te ayudará a distinguir aquello que necesitas de lo que no, y te salvará de ser presa de la publicidad y la mercadotecnia.</p>
          </li>
        </ul>

        <p class="font-weight-light">Si bien es cierto que la economía de cada familia es distinta, es importante tener en cuenta aquellas situaciones que se presentan de manera cotidiana a las que no siempre prestamos tanta atención, para poder tomar medidas sencillas que nos permitan llevar unas finanzas sanas dentro del hogar. Así que espero que estos pequeños consejos sean de utilidad la próxima vez que se te olvide cerrar la regadera mientras te enjabonas o que decidas ir a surtir tu despensa sin una lista.</p>

        <p class="font-weight-light">En Fundación Creer para Crear, estamos seguros que unas finanzas sanas en el hogar, contribuirán a una mejor calidad de vida para ti y para tu familia.</p>

